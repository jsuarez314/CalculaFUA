def suma(a,b):
  return a+b


def multiplicacion(a,b):
  return a*b
  
def calcular_potencia(a, b):
    resultado = a ** b
    return resultado

def duplica(num):
    x = num * 2
    return x

def raiz(m,n):
  return m**(1/n)

def abs(a):
  if a < 0:
    return a*-1
  else:
    return a

def division(a,b):
  return a/b
